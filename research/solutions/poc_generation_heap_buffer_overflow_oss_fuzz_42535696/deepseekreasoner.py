class Solution:
    def solve(self, src_path: str) -> bytes:
        # Extract source tarball
        with tempfile.TemporaryDirectory() as tmpdir:
            # Extract the source code
            subprocess.run(
                ['tar', '-xf', src_path, '-C', tmpdir],
                check=True,
                capture_output=True
            )
            
            # Find the extracted directory
            extracted_dirs = [d for d in os.listdir(tmpdir) 
                            if os.path.isdir(os.path.join(tmpdir, d))]
            if not extracted_dirs:
                raise RuntimeError("No extracted directory found")
            
            source_dir = os.path.join(tmpdir, extracted_dirs[0])
            
            # Build the vulnerable program
            build_dir = os.path.join(tmpdir, "build")
            os.makedirs(build_dir, exist_ok=True)
            
            # Configure and build
            configure_cmd = ['./configure', '--prefix=/tmp/ghostscript-test']
            make_cmd = ['make', '-j8']
            
            # Try to build with common build system
            if os.path.exists(os.path.join(source_dir, 'configure')):
                subprocess.run(
                    configure_cmd,
                    cwd=source_dir,
                    check=True,
                    capture_output=True
                )
                subprocess.run(
                    make_cmd,
                    cwd=source_dir,
                    check=True,
                    capture_output=True
                )
                binary_path = os.path.join(source_dir, 'bin', 'gs')
            else:
                # Try CMake
                cmake_cmd = ['cmake', source_dir, '-DCMAKE_BUILD_TYPE=Debug']
                subprocess.run(
                    cmake_cmd,
                    cwd=build_dir,
                    check=True,
                    capture_output=True
                )
                subprocess.run(
                    make_cmd,
                    cwd=build_dir,
                    check=True,
                    capture_output=True
                )
                # Look for ghostscript binary
                binary_path = self._find_ghostscript_binary(build_dir)
            
            if not os.path.exists(binary_path):
                raise RuntimeError(f"Binary not found at {binary_path}")
            
            # Generate PoC that triggers heap buffer overflow
            # Based on the vulnerability description: restore viewer state without checking depth
            # Create a PostScript/PDF that manipulates viewer state stack
            poc = self._generate_heap_overflow_poc(binary_path)
            
            return poc
    
    def _find_ghostscript_binary(self, build_dir: str) -> str:
        """Find the ghostscript binary in build directory."""
        for root, dirs, files in os.walk(build_dir):
            for file in files:
                if file in ['gs', 'gswin32c', 'gswin64c', 'ghostscript']:
                    return os.path.join(root, file)
        raise RuntimeError("Ghostscript binary not found")
    
