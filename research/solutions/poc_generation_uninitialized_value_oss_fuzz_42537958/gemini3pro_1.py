        try:
            # Attempt to locate testorig.jpg in the tarball
            with tarfile.open(src_path, 'r') as tar:
                candidates = []
                for member in tar.getmembers():
                    if member.isfile() and member.name.endswith('testorig.jpg'):
                        candidates.append(member)
                
                # If found, return the first candidate
                if candidates:
                    f = tar.extractfile(candidates[0])
                    if f:
                        content = f.read()
                        if len(content) > 0:
                            return content
        except Exception:
            # Fallback to the hardcoded PoC if extraction fails
            pass
            
        return poc